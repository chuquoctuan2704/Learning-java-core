package PrimitiveDataTypes;

public class PrimitiveDataTypes {
  public static void main(String[] args) {

    boolean isBoolean = true;
    System.out.println("isBoolean = " + isBoolean);

    char charactersValue = 'A';
    System.out.println("charactersValue = " + charactersValue);
    char integerValue = '1';
    System.out.println("integerValue = " + integerValue);

    byte byteValue = 127;
    System.out.println("byteValue = " + byteValue);
    short shortValue = 32767;
    System.out.println("shortValue = " + shortValue);
    int intValue = 2147483647;
    System.out.println("intValue = " + intValue);
    long longValue = 999999999 + 999999999;
    System.out.println("longValue = " + longValue);

    float floatValue = 21474.123F;
    System.out.println("floatValue = " + floatValue);

    double doubleValue = 99999.123D;
    System.out.println("double = " + doubleValue);
  }
}
